                <h2>Agir durablement pour le village</h2>
                <p>
Nos voyages sur le terrain nous ont permis de découvrir un autre enjeu important : ce village rencontre un problème d’hygiène dû à un surplus de déchets plastiques et aucun moyen pour les évacuer.
C'est ainsi que l'idée de la REV-Machine est née : concevoir et réaliser une chaîne de récupération de plastique pour permettre aux habitants du village de transformer un des problèmes du village en un avantage. Ce projet a pour objectif de réaliser le cycle complet du recyclage du plastique : du traitement des déchets (broyage) à la production d'objets moulés ou de câbles fin. La conception de cette machine et les tests sont en cours, l'équipe est motivée comme jamais. Réalisés en France, l'étude de la machine et les prototypages permettront aussi de sensibiliser le public de l'école au recyclage du plastique. Nous sommes en pleine phase de prototypage /expérimentation/mise au point. Notre projet s'insère donc dans une démarche d'économie circulaire car elle cherche à revaloriser un déchet non utilisé. La REV-Machine vise l'autonomie du village et un potentiel revenu économique supplémentaire pour ses habitants. Nous ne prétendons pas réaliser une innovation spectaculaire du point de vue technique mais sommes persuadés du caractère répétable de notre projet.
Le village rencontre depuis plusieurs années une dégradation accrue de l’habitat. La rénovation des maisons est donc devenue un nouvel axe pour nos récoltes de fond.
Chaque année, nous organisons donc des ventes en tout genre, des événements sportifs et caritatives et participons à des appels à projets.
Nous faisons tout pour atteindre les objectifs financiers de l’année.
Notre projet est un rêve devenu réalité et nous veillons à sa pérennité.
